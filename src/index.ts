/**
 * 加法运算
 * @param a - 加数
 * @param b - 被加数
 * @returns the result of a plus b
 * @example
 * ```ts
 * minus(1, 2) => 2
 * ```
 * 
 * @beta
 */
export function add(a: number, b: number) {
    return a + b;
}

/**
 * 减法运算
 * @param a - 减数
 * @param b - 被减数
 * @returns the result of a minus b
 * @example
 * ```ts
 * minus(3, 2) => 1
 * ```
 * 
 * @beta
 */
export function minus(a: number, b: number) {
    return a - b;
}
